    private void EmployeeViewMonthlyAttendanceReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmployeeViewMonthlyAttendanceReportActionPerformed
        User loggedInUser = Session.getInstance().getLoggedInUser();

        if (loggedInUser instanceof Employee) {
            Employee employee = (Employee) loggedInUser;

            // Prompt for year and month
            String yearStr = JOptionPane.showInputDialog(this, "Enter year (e.g., 2024):");
            String monthStr = JOptionPane.showInputDialog(this, "Enter month (1-12):");

            // Validate input
            int year, month;
            try {
                year = Integer.parseInt(yearStr);
                month = Integer.parseInt(monthStr);
                if (month < 1 || month > 12) {
                    throw new NumberFormatException("Invalid month");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid input for year or month. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Use the entered year and month to fetch the attendance report
            YearMonth selectedMonth = YearMonth.of(year, month);
            DefaultTableModel model = (DefaultTableModel) MonthlyAttendanceReport.getModel();
            model.setRowCount(0);  // Clear existing table data

            // Fetch the attendance data for the selected month
            employee.viewAttendanceReport(selectedMonth, model);

            // Update username and month in the jTextField1
            MonthlyAttendanceReportTextField.setText("Username: " + employee.getUsername() + " | Month: " + selectedMonth);

            // Show the ViewMonthly panel (Use the unique identifier)
            CardLayout cardLayout = (CardLayout) getContentPane().getLayout();
            cardLayout.show(getContentPane(), "ViewMonthly");  // Switch to Monthly attendance panel
        }
         else {
            JOptionPane.showMessageDialog(this, "Only employees can view attendance reports.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EmployeeViewMonthlyAttendanceReportActionPerformed

