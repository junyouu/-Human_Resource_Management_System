    private void hrOfficerUpdateEmployeeProfileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hrOfficerUpdateEmployeeProfileButtonActionPerformed
        // Get the logged-in user
        User loggedInUser = Session.getInstance().getLoggedInUser();

        // Check if the logged-in user is an instance of Human_Resource_Officer
        if (loggedInUser instanceof Human_Resource_Officer) {
            Human_Resource_Officer hr = (Human_Resource_Officer) loggedInUser;

            // Prompt for username
            String username = JOptionPane.showInputDialog(null, "Enter the username of the employee to update:");

            if (username != null && !username.trim().isEmpty()) {
                // Load employee data if username is found
                boolean found = hr.loadEmployeeData(username);
                if (found) {
                    // Show update window
                    hr.showEmployeeUpdateWindow(username);
                } else {
                    JOptionPane.showMessageDialog(null, "Employee not found.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Username cannot be empty.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "You do not have permission to update employee profiles.");
        }
    }//GEN-LAST:event_hrOfficerUpdateEmployeeProfileButtonActionPerformed

