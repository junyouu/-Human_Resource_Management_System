        profileDialog.add(lblName);
        profileDialog.add(valName);
        profileDialog.add(lblPhoneNumber);
        profileDialog.add(valPhoneNumber);
        profileDialog.add(lblPosition);
        profileDialog.add(valPosition);
        profileDialog.add(lblDepartment);
        profileDialog.add(valDepartment);
        profileDialog.add(lblEmergencyContact);
        profileDialog.add(valEmergencyContact);
        profileDialog.add(lblWorkingExperience);
        profileDialog.add(valWorkingExperience);
        profileDialog.add(lblHistoryOfSalary);
        profileDialog.add(valHistoryOfSalary);
        profileDialog.add(lblChangeOfPosition);
        profileDialog.add(valChangeOfPosition);
